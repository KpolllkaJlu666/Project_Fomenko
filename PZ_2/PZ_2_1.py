# Вариант 21. Дано трёхзначное число. 
# Вывести число, полученное при перестановке цифр десятков и единиц исходного числа (например, 123 → 132).

try:
    # Ввод числа
    num = int(input("Введите трёхзначное число: "))

    # Разбираем число на разряды
    sotne = num // 100          # сотни
    desyatke = (num // 10) % 10 # десятки
    edinicy = num % 10          # единицы

    # Переставляем десятки и единицы
    new_num = sotne * 100 + edinicy * 10 + desyatke

    # Вывод результата
    print("Новое число:", new_num)

except ValueError:
    print("Ошибка: нужно ввести целое число")