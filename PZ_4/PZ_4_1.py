#1. Дано вещественное число X (|X|<1) и целое число N (>0). Найти значение выражения X - X 2 /2 + X3 /3 - ... + (-1)N-1X N /N. Полученное число является приближенным значением функции ln в точке 1 + X.
try:
    x = float(input("Введите X (|X| < 1): "))
    n = int(input("Введите N (целое > 0): "))

    if (-1 < x < 1) and (n > 0):
        s = 0.0
        term = x  # первый член: x
        for k in range(1, n + 1):
            s += term
            term *= (-x) * (k / (k + 1))  # term_k -> term_{k+1}
        print("Сумма первых N членов:", s)
    else:
        print("Неверный ввод: требуется |X| < 1 и целое N > 0.")

except ValueError:
    print("Неверный ввод: требуется |X| < 1 и целое N > 0.")